package P25_0521909.interfaces;

/**
 * IInitialisable represents an object that has fields
 * and properties that need to be initialised.
 * 
 * @author Ludmila Miranda-Dukoski (ID# 0521909)
 *
 */
public interface IInitialisable {
    
    /**
     * A call to initialise the object's fields and
     * properties.
     * 
     */
    public void initialise();
    
}
